function slugify(value) {
  return value
    .toLowerCase()
    .trim()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '');
}

function parseIngredients(description) {
  return description
    .split(',')
    .map((part) => part.trim())
    .filter(Boolean);
}

function sizeOptions(small, large, family) {
  return {
    small: { enabled: true, price: small },
    large: { enabled: true, price: large },
    family: { enabled: true, price: family },
  };
}

function pizzaItem(categorySlug, number, name, description, imageIndex, pricing, badges = []) {
  const image = PIZZA_IMAGES[imageIndex % PIZZA_IMAGES.length];

  return {
    slug: `${categorySlug}-${slugify(name)}`,
    number,
    name,
    description,
    price: pricing.large,
    categorySlug,
    imageUrl: image.imageUrl,
    imageAlt: image.imageAlt,
    badges,
    ingredients: parseIngredients(description),
    sizeOptions: sizeOptions(pricing.small, pricing.large, pricing.family),
  };
}

function pastaItem(number, name, description, imageIndex, priceNote) {
  const image = PASTA_IMAGES[imageIndex % PASTA_IMAGES.length];

  return {
    slug: `pastas-${slugify(name)}`,
    number,
    name,
    description,
    price: 17,
    categorySlug: 'pastas',
    imageUrl: image.imageUrl,
    imageAlt: image.imageAlt,
    badges: [],
    ingredients: parseIngredients(description),
    priceNote: priceNote ?? null,
  };
}

function simpleItem(categorySlug, number, name, description, price, imageIndex) {
  const image = GENERAL_IMAGES[imageIndex % GENERAL_IMAGES.length];

  return {
    slug: `${categorySlug}-${slugify(name)}`,
    number,
    name,
    description,
    price,
    categorySlug,
    imageUrl: image.imageUrl,
    imageAlt: image.imageAlt,
    badges: [],
    ingredients: parseIngredients(description),
  };
}

